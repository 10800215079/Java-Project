package com.gov.Authmis.util;


public class TodayYearMonthTrans {
  private String orgcount;
  
  private String todayTransactions;
  
  private String monthTransactions;
  
  private String yearTransactions;
  
  public String getOrgcount() {
    return this.orgcount;
  }
  
  public void setOrgcount(String orgcount) {
    this.orgcount = orgcount;
  }
  
  public String getTodayTransactions() {
    return this.todayTransactions;
  }
  
  public void setTodayTransactions(String todayTransactions) {
    this.todayTransactions = todayTransactions;
  }
  
  public String getMonthTransactions() {
    return this.monthTransactions;
  }
  
  public void setMonthTransactions(String monthTransactions) {
    this.monthTransactions = monthTransactions;
  }
  
  public String getYearTransactions() {
    return this.yearTransactions;
  }
  
  public void setYearTransactions(String yearTransactions) {
    this.yearTransactions = yearTransactions;
  }
}

