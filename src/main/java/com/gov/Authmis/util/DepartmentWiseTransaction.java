public class DepartmentWiseTransaction {
  private String orgName;
  
  private String curentOrgMonth;
  
  private String curentOrgDay;
  
  public String getOrgName() {
    return this.orgName;
  }
  
  public void setOrgName(String orgName) {
    this.orgName = orgName;
  }
  
  public String getCurentOrgMonth() {
    return this.curentOrgMonth;
  }
  
  public void setCurentOrgMonth(String curentOrgMonth) {
    this.curentOrgMonth = curentOrgMonth;
  }
  
  public String getCurentOrgDay() {
    return this.curentOrgDay;
  }
  
  public void setCurentOrgDay(String curentOrgDay) {
    this.curentOrgDay = curentOrgDay;
  }
}
